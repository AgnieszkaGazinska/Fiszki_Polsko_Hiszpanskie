    //odwrócenie mapy z pol -> hiszp na hiszp -> pol
    private Map<String, String> odwroconaSlowkaMapa(Map<String, String> slowkaMapa) {
        Map<String, String> odwroconaMapa = new HashMap<>();
        for (Map.Entry<String, String> entry : slowkaMapa.entrySet()) {
            odwroconaMapa.put(entry.getValue(), entry.getKey()); // Zamiana miejscami
        }
        return odwroconaMapa;
    }
    
    
    //wyświetlanie polskich słów
    private void aktualizujSlowo() {
        String slowo = polskieSlowa.get(biezacyIndeks);
        
        if (jezyk.equals("pl")) {
            lblSlowko.setText(slowo);
        } else if (jezyk.equals("es")) {
            lblSlowko.setText(slowkaMapa.get(slowo));
        }
        lblLicznik.setText("Słówko " + (biezacyIndeks + 1) + "/" + polskieSlowa.size());
        txtOdpowiedz.setText("");
    }
    
    
    private String normalizujTekst(String tekst) {
        if (tekst == null) return "";
        tekst = Normalizer.normalize(tekst, Normalizer.Form.NFC); // Normalizacja do jednolitego formatu
        return tekst.replace("\u00A0", " ") // Zamiana twardej spacji (NBSP) na zwykłą
                    .replaceAll("\\s+", " ") // Zamiana wielu spacji na jedną
                    .trim(); // Usunięcie spacji na początku i końcu
}
    
    private void sprawdzOdpowiedz() {
        String odpowiedz = normalizujTekst(txtOdpowiedz.getText());
        String polskieSlowo = polskieSlowa.get(biezacyIndeks);
        String poprawneTlumaczenie = normalizujTekst(slowkaMapa.get(polskieSlowo));
       
        if (jezyk.equals("pl")) {
            poprawneTlumaczenie = normalizujTekst(slowkaMapa.get(polskieSlowo));    
        } else {
            Map<String, String> reversed = odwroconaSlowkaMapa(slowkaMapa);
            poprawneTlumaczenie = normalizujTekst(reversed.get(slowkaMapa.get(polskieSlowo)));
        }
        
        if (odpowiedz.equalsIgnoreCase(poprawneTlumaczenie)) { // Sprawdzenie poprawności (bez uwzględniania wielkości liter)
            lblWynik.setText("Poprawnie!");
            lblWynik.setForeground(Color.GREEN); // Zielony kolor dla poprawnej odpowiedzi
            poprawneOdpowiedzi++;
        } else {
            lblWynik.setText("Niepoprawnie! Spróbuj ponownie.");
            lblWynik.setForeground(Color.RED); // Czerwony kolor dla błędnej odpowiedzi
        }
    }


