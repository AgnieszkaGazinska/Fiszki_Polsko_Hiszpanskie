    private void btnDalejActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDalejActionPerformed
        if (biezacyIndeks < slowkaMapa.size() - 1) {
            biezacyIndeks++;
            aktualizujSlowo();
        } else {
            long czasKoncowy = System.currentTimeMillis();
            double czasNaukiSekundy = (czasKoncowy - czasStartowy) / 1000.0;

            double procentPoprawnych = (double) poprawneOdpowiedzi / slowkaMapa.size() * 100;
            procentPoprawnych = Math.round(procentPoprawnych * 100.0) / 100.0;

            // Tworzymy nowe okno z wynikami
            StatystykiFrame koniec = new StatystykiFrame(poprawneOdpowiedzi, slowkaMapa.size(), procentPoprawnych, czasNaukiSekundy);
            koniec.setVisible(true);
        }
    }//GEN-LAST:event_btnDalejActionPerformed

